--------------------------------------------------------------------------------
-- REQUIRES
--------------------------------------------------------------------------------

local ls = require("luasnip")
local utils = require("core.lib.sniputils")

--------------------------------------------------------------------------------
-- INITIALIZATION
--------------------------------------------------------------------------------

local snippet = utils.snippet

local t = ls.t
local c = ls.c
local f = ls.f

--------------------------------------------------------------------------------
-- SNIPPETS
--------------------------------------------------------------------------------

local snippets = {

    snippet("todo", "TODO Items", "<s>", {
        s = c(1, {
            t "TODO: ",
            t "FIXME: ",
            t "NOTE: ",
            t "WARN: ",
            t "HACK: ",
        })
    }),

    snippet("today", "Today's date", "<s>", {
        s = f(function()
            return os.date("%Y-%m-%d")
        end)
    }),

}

--------------------------------------------------------------------------------
-- AUTOSNIPPETS
--------------------------------------------------------------------------------

local autosnippets = {}

--------------------------------------------------------------------------------
-- RETURN
--------------------------------------------------------------------------------

return snippets, autosnippets
