--------------------------------------------------------------------------------
-- REQUIRES
--------------------------------------------------------------------------------

local ls = require("luasnip")
local utils = require("core.lib.sniputils")

--------------------------------------------------------------------------------
-- INITIALIZATION
--------------------------------------------------------------------------------

-- Utils
local snippet = utils.snippet

-- Luasnip
local i = ls.i

--------------------------------------------------------------------------------
-- SNIPPETS
--------------------------------------------------------------------------------

local snippets = {}

--------------------------------------------------------------------------------
-- AUTOSNIPPETS
--------------------------------------------------------------------------------

local autosnippets = {

    snippet("#!", "Shebang", [[
        #!/usr/bin/env bash

        <>
    ]], { i(0) }),

    snippet(";if", "If", [=[
        if [[ <cond> ]]; then
            <body>
        fi
    ]=], {
        cond = i(1, "-f $0"),
        body = i(2, "echo $0"),
    }),

    snippet(";for", "For", [[
        for <var> in <vars>; do
            <body>
        done
    ]], {
        var = i(1, "file"),
        vars = i(2, "`ls`"),
        body = i(3, "echo"),
    }),

}

--------------------------------------------------------------------------------
-- RETURN
--------------------------------------------------------------------------------

return snippets, autosnippets
